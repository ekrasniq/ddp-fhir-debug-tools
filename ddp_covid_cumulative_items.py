#!/usr/bin/env python3
"""Generate cumulative.gender for COVID with DDP-like FHIR logic."""

from __future__ import annotations

import ddp_cum_items as cum


# Set these values for your FHIR endpoint.
FHIR_BASE_URL = "http://localhost:8080/fhir"
FHIR_USER = "user"
FHIR_PASSWORD = "password"

BATCH_SIZE = 500
ID_CHUNK_SIZE = 50
USE_POST_FOR_ID_SEARCH = True
USE_ENCOUNTER_DIAGNOSIS_FOR_CONDITIONS = True
FILTER_PATIENT_RETRIEVAL = True
FILTER_RESOURCES_BY_DATE = True
MIMIC_DDP_OBS_INTERPRETATION_REMOVAL = True
USE_OBSERVATIONS = True
USE_CONDITIONS = True

COVID_START_DATE = "2020-01-27"
COVID_ICD_CODES = ["U07.1"]

COVID_PCR_LOINC_CODES = """
96957-6,94306-8,94640-0,96765-3,96763-8,96986-5,95409-9,94760-6,
94533-7,95425-5,94766-3,94316-7,97098-8,98132-4,98494-8,94559-2,
95824-9,94639-2,98131-6,98493-0,94534-5,96120-1,96123-5,96091-4,
94314-2,105749-6,94759-8,94845-5,101289-7,105748-8,94767-1,94641-8,
96448-6,96958-4,106617-4,95406-5,94565-9,96797-6,95608-6,94500-6,
94660-8,108180-1,94309-2,96829-7,94756-4,94757-2,94307-6,94308-4
""".replace("\n", "").replace(" ", "").split(",")

COVID_VARIANT_LOINC_CODES = "96895-8,96741-4,100156-9".split(",")


def main() -> None:
    cum.FHIR_BASE_URL = FHIR_BASE_URL
    cum.FHIR_USER = FHIR_USER
    cum.FHIR_PASSWORD = FHIR_PASSWORD
    cum.BATCH_SIZE = BATCH_SIZE
    cum.ID_CHUNK_SIZE = ID_CHUNK_SIZE
    cum.USE_POST_FOR_ID_SEARCH = USE_POST_FOR_ID_SEARCH
    cum.USE_ENCOUNTER_DIAGNOSIS_FOR_CONDITIONS = USE_ENCOUNTER_DIAGNOSIS_FOR_CONDITIONS
    cum.FILTER_PATIENT_RETRIEVAL = FILTER_PATIENT_RETRIEVAL
    cum.FILTER_RESOURCES_BY_DATE = FILTER_RESOURCES_BY_DATE
    cum.MIMIC_DDP_OBS_INTERPRETATION_REMOVAL = MIMIC_DDP_OBS_INTERPRETATION_REMOVAL
    cum.USE_OBSERVATIONS = USE_OBSERVATIONS
    cum.USE_CONDITIONS = USE_CONDITIONS

    cum.DISEASE_START_DATE = COVID_START_DATE
    cum.DISEASE_ICD_CODES = list(COVID_ICD_CODES)
    cum.DISEASE_POSITIVE_LOINC_CODES = list(COVID_PCR_LOINC_CODES)
    # DDP retrieval loads PCR and variant LOINCs, but positive detection uses PCR LOINCs.
    cum.DISEASE_RETRIEVAL_LOINC_CODES = list(COVID_PCR_LOINC_CODES + COVID_VARIANT_LOINC_CODES)
    cum.OUTPUT_ITEM_NAME = "cumulative.gender"

    cum.main()


if __name__ == "__main__":
    main()
